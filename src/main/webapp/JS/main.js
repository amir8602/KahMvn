function validateForm() {
    var name = document.getElementById("name").value;
    var family = document.getElementById("family").value;
    var college = document.getElementById("college").value;


    if(name != ""){
        document.getElementById("validateMsgName").style="font-weight: bolder ; color: red ; font-family: Fira Code Medium ; display: none";
    }
    if(family!= ""){
        document.getElementById("validateMsgFamily").style="font-weight: bolder ; color: red ; font-family: Fira Code Medium ; display: none";
    }
    if(college!=""){
        document.getElementById("validateMsgCollege").style="font-weight: bolder ; color: red ; font-family: Fira Code Medium ; display: none";
    }

    if (name == ""){
        document.getElementById("validateMsgName").style="font-weight: bolder ; color: red ; font-family:Fira Code Medium ; display: block";
    }
    if (family == ""){
        document.getElementById("validateMsgFamily").style="font-weight: bolder ; color: red ; font-family:Fira Code Medium ; display: block";
    }
    if (college == ""){
        document.getElementById("validateMsgCollege").style="font-weight: bolder ; color: red ; font-family:Fira Code Medium ; display: block";
    }

    if (name == undefined || family == undefined || college == undefined) {
        return false;
    }

    if (name != "" && family != "" && college != "") {
        return true;
    } else {
        return false;
    }




}